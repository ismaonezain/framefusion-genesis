/**
 * Format TRIA amount from wei (18 decimals)
 */
export function formatTriaAmount(amount: string | bigint): string {
  const value = typeof amount === 'string' ? BigInt(amount) : amount;
  const divisor = BigInt(10 ** 18);
  const result = value / divisor;
  return result.toLocaleString();
}

/**
 * Parse TRIA amount to wei (18 decimals)
 */
export function parseTriaAmount(amount: string): bigint {
  const multiplier = BigInt(10 ** 18);
  return BigInt(amount) * multiplier;
}
